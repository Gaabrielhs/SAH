    private void Entrar() {
        if (jTextFieldLogin.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite um login!");
        } else {
            if (new String(jPasswordField1.getPassword()).isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digite uma senha!");
            } else {
                fabrica.abrirConexao();
                FuncionarioDAO dao = fabrica.criarFuncionarioDAO();
                for (Funcionario f : dao.buscarTodos()) {
                    if (jTextFieldLogin.getText().equals(f.getLogin()) && new String(jPasswordField1.getPassword()).equals(f.getSenha())) {
                        visaogeral.fazerLogin(f);
                        setVisible(false);
                        limpar();
                        jTextFieldLogin.grabFocus();
                    } else {
                        jLabelInvalido.setVisible(true);
                    }
                }
                fabrica.fecharConexao();
            }
        }
    }

    private void limpar() {
        jTextFieldLogin.setText("");
        jPasswordField1.setText("");
        jLabelInvalido.setVisible(false);
    }

